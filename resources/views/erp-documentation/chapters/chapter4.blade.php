    <h6 class="mb-5" style="text-decoration: underline;">Article 5 : Dossiers de recrutement</h6>
    <p>Seul, l’Employeur a le pouvoir de procéder au recrutement des nouveaux employé(e)s.</p>
    <p>Toute personne sélectionnée en vue de recrutement ou d’embauche doit :</p>
    <p>- être âgée de plus de 18 ans</p>
    <p>- être apte psychiquement</p>
    <p>- remplir le dossier de candidature : CV, lettre de motivation, copie du plus haut diplôme, prétention
        salariale et référence professionnelle, et le dernier certificat de travail.</p>
    <p>Éventuellement, le fait de dissimuler ces faits à l’embauche rend nul et nul effet le contrat de travail
        déjà établi en cas de résurgence future.</p>
    <h6 class="mb-5" style="text-decoration: underline;">Article 6 : Dossiers à fournir lors de l’embauche</h6>
    <p>Toutes personnes embauchées doivent fournir les pièces et dossiers suivants :</p>
    <ul>
        <li>Quatre (04) photos d’identité 4 x 4</li>
        <li>Certificat de résidence de moins de trois (03) mois. En cas de changement de
            domicile, l’employé est tenu de fournir un nouveau certificat de résidence dans un
            délai d’UN (01) mois après le déménagement</li>
        <li>Photocopie de la carte d’identité nationale certifiée.</li>
        <li>Casier judiciaire, Bulletin n°3 délivré moins de trois (03) mois</li>
        <li>Acte de mariage</li>
        <li>Acte de naissance des enfants</li>
        <li>Éventuellement la carte CNAPS et / ou AMIT</li>
    </ul>
    <p>En outre, il doit satisfaire :</p>
    <ul>
        <li>Aux épreuves de sélection</li>
        <li>À une visite médicale d’aptitude auprès de l’AMIT, Service médical d’adhésion des sociétés</li>
    </ul>
    <p>Une exception est toutefois faite pour les stagiaires. Ces derniers doivent fournir :
    </p>
    <ul>
        <li>Quatre (04) photos d’identité 4 x 4</li>
        <li>Certificat de résidence délivré aux moins de trois (03) mois, ainsi qu’UN (01) mois après le
            changement de domicile pour tous les employé(e)s</li>
        <li>Photocopie de la carte d’identité nationale</li>
        <li>Casier judiciaire, Bulletin n°3 délivré moins de trois (03) mois</li>
        <li>Photocopie certifiée de l’école d’origine ou centre de formation d’origine du diplôme ou du
            certificat</li>
    </ul>
    <p>L’employé est tenu de communiquer à la société toute modification de sa situation familiale, de son
        état civil ou de son adresse, pièce justificative à l’appui.</p>
    <h6 class="mb-5" style="text-decoration: underline;">Article 7 : Période d’essai</h6>
    <p>La société « AGENCE {{ $agency }}» , n’engage à l’essai, que des personnes possédant un certificat
        de travail conforme à son poste de recrutement et/ ou ayant passé avec succès un test et un entretien
        d’embauche.</p>
    <p>La durée du contrat d’engagement à l’essai ne peut excéder celle stipulée à l’arrêté n°2007-008 du 09
        janvier 2007, fixant les formes, la durée et autres modalités relatives et au cours duquel chacune des
        parties peut librement le rompre sans préavis.
    </p>
    <p>À l’issue de la période d’essai, l’agent peut-être :
    </p>
    <p>− soit confirmé au poste si l’essai est concluant.</p>
    <p>− soit non confirmé au poste si l’essai n’est pas concluant.</p>
    <p>La période d’essai peut être interrompue par la volonté d’une des parties sans préavis ni indemnité,
        autre que celle découlant de ses droits au congé au prorata temporis.</p>
    <h6 class="mb-5" style="text-decoration: underline;">Article 8 : Ancienneté</h6>
    <p>L’ancienneté de l’employé(e) est comptée à partir de la date effective d’embauche, y compris les
        périodes d’essai effectuées par le travailleur au sein de la Société.</p>

