    <h6 class="mb-5" style="text-decoration: underline;">Article.28 : Promotion et Développement de carrière
    </h6>
    <p>La promotion consiste dans le passage à la catégorie supérieure.</p>
    <p>Tout personnel ayant les qualités requises dans l'entreprise est en droit de postuler à un
        poste à pourvoir dans la catégorie supérieure.</p>
    <p>Tout personnel ayant prouvé sa performance et sa capacité au niveau de son poste pourrait
        être promu dans la catégorie supérieure.</p>
    <p>Elle est prononcée par le département Ressources humaines.</p>
