    <h3 class="text-center my-10">
        Chapitre XIII : <span style="text-decoration: underline; text-transform: uppercase">LA DISCIPLINE GÉNÉRALE</span>
    </h3>
    <h6 class="mb-5" style="text-decoration: underline;">Article 34 : Éthique et savoir-vivre</h6>
    <p>
        Chaque salarié doit respecter les règles élémentaires de savoir-vivre et de savoir-être en
        collectivité.
    </p>
    <p>
        La tenue de chaque personnel consiste à des vêtements propres et décents pour
        l’accomplissement de son travail.
    </p>
    <p>
        Toute rixe, injure, insulte, comportement agressif, incivilité est interdit dans la société. Il en
        est de même de tout comportement raciste, xénophobe, sexiste et/ou discriminant au sens
        des dispositions du Code du travail et du Code pénal.
    </p>
    <p>
        Il est interdit de pénétrer ou de demeurer dans l’établissement en état d’ivresse ou sous
        l’emprise de la drogue.
    </p>
    <p>
        De même, il est formellement interdit de fumer dans l’enceinte de la société.
    </p>
    <h6 class="mb-5" style="text-decoration: underline;">Article 35 : Harcèlement sexuel</h6>
    <p>
        Conformément au Code du travail, aucun salarié, aucun candidat à un recrutement, à un
        stage ou à une période de formation en entreprise ne peut être sanctionné, licencié ou faire
        l’objet d’une mesure discriminatoire, directe ou indirecte, notamment en matière de
        rémunération, de formation, de reclassement, d’affectation, de qualification, de classification,
        de promotion professionnelle, de mutation ou de renouvellement de contrat pour avoir subi
        ou refusé de subir les agissements de harcèlement de toute personne dont le but est
        d’obtenir des faveurs de nature sexuelle à son profit ou au profit d’un tiers.
    </p>
    <p>
        Aucun salarié ne peut être sanctionné, licencié ou faire l’objet d’une mesure discriminatoire
        pour avoir témoigné des agissements définis à l’alinéa précédent ou pour les avoir relatés
    </p>
    <p>
        En revanche, est passible d’une sanction disciplinaire tout salarié qui, dans l’exercice de ses
        fonctions, aura procédé à de tels agissements.
    </p>
    <p>
        Aucun salarié ne peut être sanctionné, licencié ou faire l'objet d'une mesure discriminatoire,
        directe ou indirecte, notamment en matière de rémunération, de formation, de reclassement,
        d'affectation, de qualification, de classification, de promotion professionnelle, de mutation ou
        de renouvellement de contrat pour avoir subi ou refusé de subir des agissements répétés de
        harcèlement moral ou pour avoir témoigné de tels agissements ou les avoir relatés.
    </p>
    <h6 class="mb-5" style="text-decoration: underline;">Article 36 : Harcèlement moral</h6>
    <p>
        Aucun salarié ne doit subir les agissements répétés de harcèlement moral qui ont pour objet
        ou pour effet une dégradation des conditions de travail susceptible de porter atteinte à ses
        droits et à sa dignité, d’altérer sa santé physique ou mentale ou de compromettre son avenir
        professionnel.
    </p>
    <p>
        Aucun salarié ne peut être sanctionné, licencié ou faire l’objet d’une mesure discriminatoire
        directe ou indirecte, notamment en matière de rémunération, de formation, de reclassement,
        d’affectation, de qualification, de classification, de promotion professionnels, de mutation, ou
        de renouvellement de contrat pour avoir subi ou refusé de subir des agissements constitutifs
        de harcèlement moral ou pour avoir témoigné de tels agissements ou les avoir relatés.
    </p>
    <p>
        En revanche, est passible d’une sanction disciplinaire tout salarié qui, dans l’exercice de ses
        fonctions, aura procédé à de tels agissements.
    </p>
    <p>
        Aucun salarié ne doit subir les agissements répétés de harcèlement moral qui ont pour objet
        ou pour effet une dégradation de ses conditions de travail susceptible de porter atteinte à ses
        droits et à sa dignité, d'altérer sa santé physique ou mentale ou de compromettre son avenir
        professionnel.
    </p>
    <h6 class="mb-5" style="text-decoration: underline;">Article 37 : Départ du bureau</h6>
    <p>
        Le personnel est prié, lorsqu’il quitte son bureau le soir, d’éteindre tous les appareils
        électriques se trouvant dans son bureau.
    </p>
    <p>
        La dernière personne partant le soir est priée de veiller à ce que toutes les lumières et tous
        les appareils électriques soient éteints. Elle devrait s’assurer de la fermeture du bureau.
    </p>
