    </h3>
    <h6 class="mb-5" style="text-decoration: underline;">Article.9 : Formation</h6>
    <p>L’employeur s’efforcera de promouvoir l’épanouissement social de chaque employé et de faciliter le
        développement de la formation professionnelle de son personnel.</p>
    <p>La formation sera soumise à l’appréciation du chef hiérarchique selon les besoins de la société et/ou
        de l’employé, d’une part, et du budget de formation d’autre part.</p>
    <p>Au cas où le nombre de places est limité, il pourra être tenu compte de l’ancienneté dans le poste.
    </p>
    <h6 class="mb-5" style="text-decoration: underline;">Article.10 : Promotion interne</h6>
    <p>En cas de vacance de poste ou de création de postes, et dans la mesure du possible, l'employeur
        favorise en priorité le recrutement interne en examinant les candidatures du personnel répondant au
        profil requis.</p>
    <p>Seront étudiées, les candidatures :</p>
    <ul>
        <li>De l’agent, quelle que soit la catégorie professionnelle mais ayant le diplôme requis au poste
            vacant.</li>
        <li>Des agents titulaires de certificat ou attestation de formation professionnelle dans la catégorie
            professionnelle concernée.
        </li>
        <li>De l’agent ayant assuré l’intérim du poste pendant une période de plus de trois mois
            consécutifs ou non après évaluation de ses performances ou de son mérite. Cet intérim est
            suivi d’une période de probation de 3 (trois) mois.</li>
        <li>De l’agent de la catégorie immédiatement inférieure au poste, ayant effectué plus de 3 (trois)
            ans de service.
        </li>
    </ul>
    <p>Si l’essai est concluant, l’agent est confirmé d’office dans la classification correspondant au poste
        occupé. Si l’essai ne s’avère pas concluant, l’agent réintègre son ancien poste et perdra le bénéfice
        de l’indemnité de période d’essai.
    </p>
    <h6 class="mb-5" style="text-decoration: underline;">Article.11 : Classification professionnelle</h6>
    <p>Les définitions de chaque catégorie professionnelle sont celles prévues par le règlement en vigueur.</p>
    <p>Le responsable personnel ou le chef hiérarchique est seul habilité à notifier le classement qui
        convient à un agent, après étude de chaque dossier par les responsables compétents.</p>
</div>
