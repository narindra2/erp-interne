    <h3 class="text-center my-10">
        Chapitre III : <span style="text-decoration: underline; text-transform: uppercase">LIBERTÉ D’OPINION</span>
    </h3>
    <h6 class="mb-5" style="text-decoration: underline;">Article 4 :</h6>
    <p>
        Nous reconnaissons la liberté d’opinion ainsi que la liberté de s’associer pour la défense des
        intérêts collectifs afférents à leurs conditions d’employés et d’employeur.
    </p>
    <p>
        En aucun cas, les décisions prises, notamment celles concernant l’embauche, la répartition
        du travail, la formation professionnelle, la discipline générale, l’avancement, l’application des
        sanctions et des licenciements ne peuvent se fonder sur le fait que l’intéressé appartient ou
        non à un organisme syndical, à un organisme professionnel quelconque, à une partie
        politique ou à une religion.
    </p>
    <p>L’employeur ou ses représentants ne doivent user d’aucun moyen de pression aussi bien en
        faveur qu’à l’encontre d’une quelconque organisation des travailleurs.</p>
    <p>
        En cas de violation des dispositions du présent article, les représentants et/ou l’ensemble du
        personnel pourront saisir l’Employeur ou ses représentants. Ces représentants et/ou
        l’ensemble du personnel s’emploieront à établir les faits afin d’obtenir en faveur de l’employé
        lésé la réintégration à son poste.
    </p>
    <p>
        Ces communications doivent conserver un caractère strictement professionnel.
    </p>
</div>
