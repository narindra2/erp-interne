<div class="row mb-10">
    <h3 class="text-center my-10">
        CHAPITRE XI : <span style="text-decoration: underline; text-transform: uppercase">SANCTIONS
        </span>
    </h3>
    <p>Tout agent est sanctionné par suite d’agissement considéré par l’employeur comme fautif ou
        tout manquement à la discipline. Suivant la gravité de la faute, le personnel peut être
        sanctionné suivant les sanctions suivantes lesquelles ne constituent pas une progression
        obligatoire, et ce, dans le respect des dispositions légales et réglementaires, notamment en
        matière d’entretien préalable :</p>
    <h6 class="mb-5" style="text-decoration: underline;">Article.29 : Sanctions légères</h6>
    <ul>
        <li><b>Avertissement verbal :</b> </li>
        <table table="table table-row-dashed table-row-gray-300 gy-5">
            <tr>
                <td>Retard répétitif sans justificatif (5 retards)</td>
            </tr>
            <tr>
                <td>Absences d'une journée sans motif justifié</td>
            </tr>
            <tr>
                <td>Non atteinte des objectifs demandés en qualité et en productivité
                </td>
            </tr>
            <tr>
                <td>Altération bien de la société</td>
            </tr>
            <tr>
                <td>Refus d’ exécuter les tâches et délais demandés par le supérieur</td>
            </tr>
            <tr>
                <td>Quitter le lieu de travail sans accord auprès des RH.</td>
            </tr>
            <tr>
                <td>Ne pas effectuer les 40h de travail conclu par semaine (sauf cas exceptionnel)</td>
            </tr>
            <tr>
                <td>Partir en congé sans validation</td>
            </tr>
            <tr>
                <td>Pause abusive sans pointage</td>
            </tr>
            <tr>
                <td>Utilisation abusive de la connexion internet sans demande au préalable (visionnage
                    film, facebook, youtube, etc…)</td>
            </tr>
        </table>
    </ul>
    <ul>
        <li>Avertissement écrit : récidive après 3 avertissements verbaux.
        </li>
    </ul>
    <h6 class="mb-5" style="text-decoration: underline;">Article.30 : Sanctions sévères</h6>
    <ul>
        <li>Mise à pied de 1 à 3 jours :
        </li>
        <table class="table table-row-dashed table-row-gray-300 gy-5">
            <tr>
                <td>Insubordination</td>
            </tr>
            <tr>
                <td>Manque de respect</td>
            </tr>
            <tr>
                <td>Refus d’exécuter une tâche faisant partie de ses attributions</td>
            </tr>
        </table>
    </ul>
    <h6 class="mb-5" style="text-decoration: underline;">Article.31 : Sanctions graves</h6>
    <ul>
        <li>Mise à pied de 4 à 10 jours ou Licenciement immédiat.</li>
    </ul>
    <table class="table table-row-dashed table-row-gray-300 gy-5">
        <tr>
            <td>Effectuer un travail en dehors de ceux confiés par la société.
            </td>
        </tr>
        <tr>
            <td>Contacter et démarcher les clients de la société directement ou
                indirectement
            </td>
        </tr>
        <tr>
            <td>Incompétence flagrante et inacceptable</td>
        </tr>
        <tr>
            <td>Vol et fraude
            </td>
        </tr>
        <tr>
            <td>État d’ivresse</td>
        </tr>
    </table>
    <tr>
        <td>Abandon de poste(3 jours d'absences sans motif justifié)</td>
    </tr>
    <p>Le salarié a la possibilité de se faire assister par un autre représentant du Personnel.</p>
    <p>L’employeur se réserve le droit d’apprécier la gravité de toute faute commise et des
        sanctions à appliquer sous le seul contrôle éventuel de la juridiction compétente notamment
        en cas de faute lourde privative de préavis.</p>
    <h6 class="mb-5" style="text-decoration: underline;">Article.32 : Amnistie</h6>
    <p>Une sanction – avertissement et mise à pied – est annulée et retirée du dossier à l’expiration
        d’un délai de 4 ans à compter de la notification.
    </p>
    <p>L’Employeur doit notifier l’amnistie.</p>
