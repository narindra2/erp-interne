    <h3 class="text-center my-10">
        CHAPITRE XI : <span style="text-decoration: underline; text-transform: uppercase">SANCTIONS
        </span>
    </h3>
    <p>Tout agent est sanctionné par suite d’agissement considéré par l’employeur comme fautif ou tout
        manquement à la discipline. Suivant la gravité de la faute, le personnel peut être sanctionné suivant
        les sanctions suivantes lesquelles ne constituent pas une progression obligatoire, et ce, dans le
        respect des dispositions légales et réglementaires, notamment en matière d’entretien préalable :</p>
    <h6 class="mb-5" style="text-decoration: underline;">Article.29 : Sanctions légères</h6>
    <ul>
        <li><b>Avertissement verbal :</b> </li>
        <table classs="table table-row-dashed table-row-gray-300 gy-5">
            <tr>
                <td>- Retard non justifié (Non respect des heures de travail)</td>
            </tr>
            <tr>
                <td>- Absences d'une journée sans motif justifié (Irresponsabilité et non respect des procédures)</td>
            </tr>
            <tr>
                <td>- Partir en congé sans validation. (Non respect des procédures)</td>
            </tr>
            <tr>
                <td>- Altération des biens de la société (Irresponsabilité professionnelle)</td>
            </tr>
            <tr>
                <td>- Refus d’exécuter les tâches et délais demandés par le supérieur (Carence professionnelle)
                </td>
            </tr>
            <tr>
                <td>- Ne pas effectuer les 40h de travail conclu par semaine (sauf cas exceptionnel
                    demandé/justifié/validé par écrit) (Non respect des heures de travail)</td>
            </tr>
            <tr>
                <td>- Utilisation abusive de la connexion internet sans demande au préalable (visionnage film, jeux,
                    facebook, youtube, etc…) (Irresponsabilité professionnelle)</td>
            </tr>
        </table>
    </ul>
    <h6 class="mb-5" style="text-decoration: underline;">Article.30 : Sanctions sévères</h6>
    <ul>
        <li>Avertissement écrit : 
            {{-- récidive après 3 avertissements verbaux. --}}
        </li>
        <table classs="table table-row-dashed table-row-gray-300 gy-5">
            <tr>
                <td>- Récidive après 3 avertissements verbaux</td>
            </tr>
            <tr>
                <td>- Pause abusive sans pointage (Non respect des procédures et des heures de travails)</td>
            </tr>
            <tr>
                <td>- Quitter le lieu de travail sans accord auprès des supérieurs hiérarchique (Non respect des
                    procédures)</td>
            </tr>
            <tr>
                <td>- Trois retards non justifiés (Non respect des procédures et des heures de travails)</td>
            </tr>
            <tr>
                <td>- Non atteinte des objectifs demandés en qualité et en productivité (Carence professionnelle)</td>
            </tr>
        </table>
    </ul>
    <ul>
        <li>Mise à pied de 1 à 3 jours :
        </li>
        <table classs="table table-row-dashed table-row-gray-300 gy-5">
            <tr>
                <td>- Insubordination</td>
            </tr>
            <tr>
                <td>- Manque de respect</td>
            </tr>
            <tr>
                <td>- Refus d’exécuter une tâche faisant partie de ses attributions</td>
            </tr>
            <tr>
                <td>- État d’ivresse</td>
            </tr>
        </table>
    </ul>
    <h6 class="mb-5" style="text-decoration: underline;">Article.31 : Sanctions graves</h6>
    <ul>
        <li>Mise à pied de 08 jours ou Licenciement immédiat :</li>
    </ul>
    <table classs="table table-row-dashed table-row-gray-300 gy-5">
        <tr>
            <td> - Effectuer un travail en dehors de ceux confiés par la société.
            </td>
        </tr>
        <tr>
            <td> - Contacter et démarcher les clients de la société directement ou
                indirectement
            </td>
        </tr>
        <tr>
            <td> - Incompétence flagrante et inacceptable</td>
        </tr>
        <tr>
            <td> - Vol et fraude
            </td>
        </tr>
        <tr>
            <td> - Abandon de poste (3 jours d’absence sans motif justifié).
            </td>
        </tr>
       
    </table>
 
