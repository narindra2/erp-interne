    <h6 class="mb-5" style="text-decoration: underline;">Article 16 : Horaires de travail</h6>
    <p>Les horaires de travail dans la société AGENCE {{ $agency }} sont flexibles, c'est-à-dire que les
        employés sont libres de choisir quotidiennement leur heure d’entrée, leur prise de pause et leur heure
        de sortie. Par contre, ces intervalles d'heures flexibles sont réglementés par des plages horaires que
        tous les employés doivent respecter <strong> sans dépasser les 40 heures par semaine.</strong> Ces plages
        horaires sont :</p>
    <ul>
        <li>Plage horaire pour les entrées: de 7 heures 00 minutes à 10 heures 00 minutes , sauf les
            opérateurs call.
        </li>
        <li>Plage horaire pour les sorties: de 16 heures 00 minutes à 19 heures 30 minutes.
        </li>
        <li>Plage horaire pour la pause déjeuner qui est obligatoire : entre midi et 14 heures.
        </li>
        {{-- <li>pour les sorties: de 16 heures à 19 heures 30 minutes sauf récupération
            d’heure ou demande spécifique auprès du Responsable Ressources
            Humaines</li>
        <li>pause déjeuner : de 30 minutes à 1 heure ( entre midi et 14 heures).</li> --}}
    </ul>
    <p>De même, nul ne peut quitter son poste de travail avant l’heure marquant la fin du travail qui est fixé à
        16 heures 00 minutes.</p>
    <h6 class="mb-5" style="text-decoration: underline;">Article 17 : Pauses</h6>
    <p>La prise de pause pendant les intervalles d’heure de travail (hors des plages horaires flexibles
        mentionnées dans l’article 16) est classifiée en deux (02) catégories.</p>
       
        <li>Pause régulière : prendre une pause sans dépasser 30 minutes.
        </li>
        <li>Pause irrégulière : si un employé est contraint de prendre une pause d’une durée supérieure
            à 30 minutes, ce dernier est obligé d'informer son supérieur hiérarchique.
        </li>
    <h6 class="mb-5" style="text-decoration: underline;">Article 18 : Sécurité</h6>
    <p>La société reste garante de la sécurité de ses personnels durant l'horaire de travail.</p>
    <h6 class="mb-5" style="text-decoration: underline;">Article 19 : Retard</h6>
    <p>Un avertissement pour non-respect des heures de travail sera cautionné au personnel ayant cumulé
        15 retards justifiés en 02 mois consécutifs.</p>
