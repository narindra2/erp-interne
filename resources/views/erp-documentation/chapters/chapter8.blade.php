<div class="row mb-10">
    <h3 class="text-center my-10">
        Chapitre VIII : <span style="text-decoration: underline; text-transform: uppercase">LES HORAIRES DE
            TRAVAIL</span>
    </h3>
    <h6 class="mb-5" style="text-decoration: underline;">Article 16 : Horaires de travail</h6>
    <p>Tout(e)s les employé(e)s doivent respecter les horaires de travail qui sont:</p>
    <ul>
        <li>pour les entrées: de 7 heures 30 minutes à 9 heures 30 minutes , sauf les
            opérateurs call de 10 heures 30 minutes
        </li>
        <li>pour les sorties: de 16 heures à 19 heures 30 minutes sauf récupération
            d’heure ou demande spécifique auprès du Responsable Ressources
            Humaines</li>
        <li>pause déjeuner : de 30 minutes à 1 heure ( entre midi et 14 heures).</li>
    </ul>
    <p>De même, nul ne peut quitter son poste de travail avant l’heure marquant la fin du travail</p>
    <h6 class="mb-5" style="text-decoration: underline;">Article 17 : Pauses</h6>
    <p>L’organisation de la pause est laissée sous la responsabilité du département Ressources
        Humaines.</p>
    <h6 class="mb-5" style="text-decoration: underline;">Article 18 : Sécurité</h6>
    <p>La société reste garante de la sécurité de ses personnels durant l'horaire de travail.</p>
    <h6 class="mb-5" style="text-decoration: underline;">Article 19 : Retard</h6>
    <p>Un avertissement verbal sera cautionné au personnel dont le retard continu est constaté.</p>
    <p>En cas de récidive après l'avertissement, le personnel sera sanctionné.</p>
    <ul>
        <li>Avertissement verbal: après 5 retards dans le mois (sans justificatif)</li>
        <li>Avertissement écrit: plus de 10 retards</li>
    </ul>
</div>
