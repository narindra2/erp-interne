    <h6 class="mb-5" style="text-decoration: underline;">N.B:</h6>
    <p>En l'absence de présentation des pièces justificatives dans le délai spécifié, ou en cas de
        non-communication préalable des événements (sauf en cas de force majeure), ou si les pièces
        justificatives ne respectent pas les normes de validation établies par la société ou sont falsifiées, les
        absences seront déduites du salaire.
    </p>
    <p>Les absences pour événements familiaux sont prises au moment où intervient l’événement.</p>
    <p>(*) : Il ne sera pris en considération une 2ème attestation du Fokontany en cas de remariage selon les
        us et coutumes que sur présentation d’une attestation de rupture délivrée par le même Fokontany.</p>
    <p>(**) : L’absence pour motif d’assistance médical d’un enfant est régi par l’article 13 alinéa 4 du code
        de travail malagasy.</p>
    <p>Toute absence non stipulée dans le présent document fera l’objet d’une décision de la Direction
        générale (programme électorale, convocation Fokontany, …)</p>
    <h6 class="mb-5" style="text-decoration: underline;">Article 22 : Événements exceptionnels</h6>
    <p>En cas d’événements exceptionnels urgents avec preuves à l’appui, l’employé(e) peut faire une
        demande de disponibilité sans solde qui ne peut excéder TROIS (03) JOURS.</p>
    <h6 class="mb-5" style="text-decoration: underline;">Article.23 : Congé de maternité</h6>
    <h6 class="mb-5" style="text-decoration: underline;">Article.23.1 : Salaire de congé de maternité</h6>
    <p>Si affilié, durant le congé de maternité, 50% de la totalité du salaire sera payé par la société et le
        reste de 50% sera dû par la CNAPS.</p>
    <h6 class="mb-5" style="text-decoration: underline;">Article.23.2 : Grossesse</h6>
    <p>Toute femme enceinte dont l’état a été constaté par le Médecin ou dont la grossesse est apparente
        peut quitter le travail sans préavis et sans avoir de ce fait à payer une indemnité de rupture de
        contrat.</p>
    <h6 class="mb-5" style="text-decoration: underline;">Article.23.3 : Accouchement</h6>
    <p>Elle a droit à 14 semaines consécutives de congés de maternité, soit 6 semaines de congé prénatal
        et 8 semaines de congé postnatal. Les 8 semaines peuvent être prolongées de trois semaines en cas
        de maladie dûment constatée et résultant de la grossesse ou des couches.</p>
    <h6 class="mb-5" style="text-decoration: underline;">Article.23.4 : Allaitement</h6>
    <p>Durant la période de 15 mois, à compter de la naissance de l’enfant, la mère a droit à 1 heure non
        travaillée, mais rémunérée pour allaitement par journée de travail.</p>
    <p>Durant cette période, la mère peut quitter la société sans préavis, et sans avoir payé une
        indemnité de rupture.</p>
    <h6 class="mb-5" style="text-decoration: underline;">Article 24 : Suspension de contrat</h6>
    <p>Le contrat de travail est suspendu en cas de :</p>
    <ul>
        <li>Longues maladies,</li>
        <li>Accidents de travail, handicapant
        </li>
        <li>Occupation à une fonction élective,</li>
        <li>Départ de l’employeur ou de l’employé(e) sous le drapeau,</li>
        <li>Détention préventive,</li>
        <li>Chômage technique,</li>
        <li>Grève licite.</li>
    </ul>
    <p>Le contrat est également suspendu en cas de maladie grave dûment constatée par le médecin du
        Service médical d’Entreprise attirée à la Société, laquelle la durée n’excédant pas SIX (06) MOIS.
        Passé ce délai, l’Employeur peut rompre le contrat, mais doit régler tous les droits du travailleur, dont
        notamment le préavis.</p>
    <p>Pendant cette période l’employé(e) a droit à un (01) mois de salaire complet si le préavis est inférieur
        ou égal à UN (01) mois, mais si la durée de la maladie est supérieure à UN (01) mois, il lui est
        attribué un préavis proportionnel à sa catégorie professionnelle à sa catégorie professionnelle et son
        ancienneté au service prévu par la législation en vigueur.</p>
    <p>L’employé(e) est tenu de présenter ou de remettre à son chef hiérarchique les pièces justificatives y
        afférentes, au plus tard CINQ (05) JOURS qui suivent la maladie pour qu’il puisse s’acquitter du droit
        énuméré ci-dessus.</p>
    <h6 class="mb-5" style="text-decoration: underline;">Article 25 : Rupture de contrat</h6>
    <p>Le contrat de travail est rompu pour les causes énumérées ci-après :</p>
    <ul>
        <li>Du fait de l’employeur, par licenciement
        </li>
        <li>Du fait du travailleur, par la démission</li>
        <li>Par accord de volonté des parties</li>
        <li>En cas de force majeure laissée à l’appréciation de la juridiction compétente</li>
        <li>Départ de l’employé(e) à la retraite</li>
        <li>Décès de l'employé(e)</li>
    </ul>
    <h6 class="mb-5" style="text-decoration: underline;">Article 26 : Suspension de salaire</h6>
    <p>Le salaire est suspendu en fonction des cas et modalités d’application énumérés dans l’article 13 et
        14 du code de travail malgache en vigueur.</p>
