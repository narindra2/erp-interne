    <h6 class="mb-5" style="text-decoration: underline;">Article.20 : Congés annuels</h6>
    <p>Le personnel a droit à un congé annuel de 30 jours calendaires ou 2,5 jours calendaires par mois de
        service effectif.</p>
    <p>Le droit se prescrit par trois (03) ans. Il appartient à l'employeur de planifier le départ en congé des
        travailleurs en début d'année.</p>
    <p>Ce droit de jouissance est acquis après 12 mois de service effectif. Sont assimilés à un mois de
        service effectif, les périodes équivalentes à 4 semaines. La période de référence est comprise entre
        le 1er janvier et le 31 décembre de l’année précédente.</p>
    <p>Dispositions relatives à l’attribution des congés :</p>
    <p>Tout départ en congé doit faire l’objet d’une demande et d’un enregistrement à la fiche individuelle au
        registre d’employeur une (1) semaine avant son octroi.</p>
    <p>Les congés ont un caractère obligatoire, par conséquent, la Société ne sera pas admise à employer
        le personnel en congé.</p>
    <p>Seulement, en cas de rupture ou d’expiration du contrat avant que le travailleur ait exercé ses droits
        au congé, qu’une indemnité calculée sur la base des droits acquis doit être accordée au lieu et place
        du congé.</p>
    <p>En dehors de ces cas dorénavant, est nulle et de nul effet toute convention prévoyant l’octroi d’une
        indemnité compensatrice au lieu et place du congé.</p>
    <h6 class="mb-5" style="text-decoration: underline;">Article 21 : Permission</h6>
    <p>Toutes demandes de permission d’absence doivent avoir préalablement l’avis du département
        Ressources Humaines CINQ (05) JOURS PRÉCÉDANT la date de départ prévue à la demande pour
        les événements prévisibles. Le départ ne doit y avoir lieu qu’après justification et validation de la
        demande par le département Ressources Humaines. Le droit de permission d’un employé est fixé à
         <strong>10 Jours par année</strong> .</p>
