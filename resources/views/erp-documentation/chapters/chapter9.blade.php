    <h6 class="mb-5" style="text-decoration: underline;">Article.27 : Litige</h6>
    <p>Les différends relatifs à l’application du présent Règlement intérieur qui n’auraient pu être tranchés à
        l’amiable entre la Direction et les Représentants du Personnel seront transmis à l’Inspection du
        Travail.</p>
        <p>Dans ce cas, c’est la législation du travail en vigueur qui est applicable.</p>
