    <h6 class="mb-5" style="text-decoration: underline;">Article 12 : Dossiers personnels
    </h6>
    <p>Pour l’administration des dossiers personnels, des mises à jour systématiques sont à procéder. Ainsi,
        chaque travailleur devrait justifier les événements par des pièces justificatives.
    </p>
