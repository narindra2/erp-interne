    <h6 class="mb-5" style="text-decoration: underline;">Article 13 : Évaluations
    </h6>
    <p>Des évaluations mensuelles du personnel en fonction de la production et qualité seront établies par
        chaque responsable hiérarchique. Cela est dû afin de considérer la performance et la compétence de
        chaque individu.</p>
