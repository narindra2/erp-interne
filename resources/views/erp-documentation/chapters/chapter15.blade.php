<div class="row mb-10">
    <h3 class="text-center my-10">
        CHAPITRE XV : <span style="text-decoration: underline; text-transform: uppercase">LE RÈGLEMENT SPÉCIFIQUE</span>
    </h3>
 
    <p>En complément du Règlement Intérieur, chaque département et chaque projet au sein de la société
        est assujetti à des Règlements Spécifiques. Ces derniers sont élaborés dans le dessein d'améliorer
        de manière continue la qualité des prestations fournies et de promouvoir le développement des
        compétences des collaborateurs.
    </p>
    <p>Ils ont pour objectif de régir l'exercice des fonctions internes, de prévenir tout préjudice potentiel
        susceptible d'altérer les engagements contractuels pris par la société, et de renforcer la responsabilité
        professionnelle de chaque membre au sein de l'organisation.</p>
</div>
