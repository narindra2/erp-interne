    <h6 class="mb-5" style="text-decoration: underline;">Article 33 : Matériels de la société
    </h6>
    <p>Sauf autorisation particulière, les locaux et le matériel de la société doivent être exclusivement
        réservés aux activités professionnelles.
    </p>
    <p>Il est notamment interdit d’emporter, même pour quelques jours seulement, des objets appartenant à
        la société, sauf accord préalable du département ressource humaine.</p>
    <p>Interdiction d’utiliser au local son PC personnel.</p>
    <p>Tout salarié est tenu de conserver en bon état le matériel qui lui est confié pour l’exécution de son
        travail.</p>
    <p>La détérioration ou le mauvais fonctionnement des matériels de la société dont le personnel aurait
        connaissance doivent être immédiatement signalés à son chef hiérarchique.
    </p>
