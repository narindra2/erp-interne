    <h6 class="mb-5" style="text-decoration: underline;">Article 38 : Sanctions et modes d’application</h6>
    <p>
        Tout agissement considéré comme fautif pourra, en fonction de sa gravité, faire l’objet de
        l’une ou l’autre des sanctions suivantes :
    </p>
    <ul>
        <li>Avertissement écrit (émargé par l’intéressé ou envoyé par lettre recommandée avec
            Accusé de Réception) ;
        </li>
        <li>
            Mise à pied disciplinaire d’un à trois jours, sans rémunération ;
        </li>
        <li>
            Licenciement pour faute ;
        </li>
    </ul>
    <p>
        Est considéré comme fautif un comportement qui se manifeste par un acte positif ou une
        abstention de nature volontaire ne correspondant pas à l’exécution normale de la relation
        contractuelle. Il peut s’agir du non-respect d’une disposition du règlement intérieur, du Code
        du travail, mais aussi de l’inexécution ou de la mauvaise exécution du travail.
    </p>
    <p>
        Aucun fautif ne peut donner lieu, à lui seul, à l’engagement de poursuites disciplinaires
        au-delà d’un délai de deux mois à compter du jour où l’employeur en a eu connaissance, à
        moins que ce fait n’ait donné lieu dans le même délai à l’exercice de poursuites pénales.
    </p>
    <p>
        Aucune sanction antérieure de plus de trois ans à l'engagement des poursuites disciplinaires
        ne peut être invoquée à l'appui d'une nouvelle sanction.
    </p>
    <p>
        Aucune sanction ne peut être infligée au salarié, sans qu’il ne soit informé dans le même
        temps et par écrit des griefs retenus contre lui.
    </p>
    <p>
        Toute sanction autre qu’un avertissement ne pourra être décidée ou appliquée, tant que
        l’intéressé n’aura pas été dûment appelé et entendu.
    </p>
    <p>
        À la suite de cet entretien, la sanction éventuelle lui sera notifiée par un écrit motivé, au
        moins un jour franc et au plus un mois après l’entretien préalable.
    </p>
